#include "pi_aes_ctr.hpp"
#include <vector>
#include <gtest/gtest.h>

TEST(PI_CryptoTests, TestAesCtr) {
    // 9A265AB6D907A5E6E820EA0D5CC1D25F9A265AB6D907A5E6E820EA0D5CC1D25F
    // XOR mask: 1e170d15f7cfa948fb6b13365f76b08c712d9f50cf72b18863e02f569bb3c8e3
    uint8_t key[PI_AES_KEYLEN] = {
        0x84,0x31,0x57,0xa3,0x2e,0xc8,0x0c,0xae,
        0x13,0x4b,0xf9,0x3b,0x03,0xb7,0x62,0xd3,
        0xeb,0x0b,0xc5,0xe6,0x16,0x75,0x14,0x6e,
        0x8b,0xc0,0xc5,0x5b,0xc7,0x72,0x1a,0xbc
    };

    // 4E9AA41C95DB51E5A958FFD9A4B65655
    uint8_t iv[PI_AES_BLOCKLEN] = {
        0x4E, 0x9A, 0xA4, 0x1C, 0x95, 0xDB, 0x51, 0xE5,
        0xA9, 0x58, 0xFF, 0xD9, 0xA4, 0xB6, 0x56, 0x55
    };

    // small plaintext
    unsigned char short_input[4] = { 0x74, 0x65, 0x73, 0x74 }; // "test"
    unsigned char short_want[4] = { 0xf9, 0x77, 0xe2, 0x2f };
    PI_AesCtrCrypt(key, iv, short_input, sizeof(short_input));
    EXPECT_EQ(memcmp(short_input, short_want, sizeof(short_input)), 0);
    PI_AesCtrCrypt(key, iv, short_input, sizeof(short_input));
    EXPECT_EQ(memcmp(short_input, "test", sizeof(short_input)), 0); // test decryption

    // long plaintext
    unsigned char long_input[100] = {
        0x96, 0xe3, 0x2d, 0x06, 0x32, 0x67, 0x74, 0x47,
        0xa0, 0x16, 0x23, 0xfb, 0x54, 0x9a, 0x52, 0xd9,
        0xf6, 0x86, 0x88, 0x80, 0x05, 0x68, 0x0f, 0x7c,
        0xa5, 0x8e, 0x1f, 0x38, 0x5a, 0xd0, 0x0b, 0x88,
        0x47, 0x2a, 0xd7, 0x0c, 0x2b, 0x34, 0x5a, 0x51,
        0xad, 0x3c, 0x92, 0xb9, 0x07, 0xdc, 0x61, 0x43,
        0x38, 0x31, 0xe2, 0x9f, 0x8c, 0x18, 0x01, 0x2b,
        0x2a, 0xa9, 0x6b, 0x93, 0xdc, 0xba, 0x8d, 0xbe,
        0x84, 0xe5, 0x8c, 0x18, 0xc2, 0xc4, 0x30, 0x98,
        0x54, 0x25, 0xb8, 0x22, 0x1f, 0x07, 0xf6, 0x72,
        0xb1, 0x0d, 0x3e, 0xc4, 0xcb, 0x05, 0xaa, 0x19,
        0x41, 0xab, 0x64, 0x9c, 0xa5, 0xb9, 0x7b, 0xca,
        0x81, 0xfb, 0x3c, 0x6c
    };
    unsigned char long_plaintext[100];
    memcpy(long_plaintext, long_input, sizeof(long_plaintext));
    unsigned char long_want[100] = {
        0x1b, 0xf1, 0xbc, 0x5d, 0x47, 0xf5, 0x51, 0x2b,
        0x79, 0x76, 0x4c, 0xc9, 0xae, 0x9a, 0x44, 0xb8,
        0x10, 0xff, 0x12, 0x10, 0xfd, 0x8e, 0x1c, 0x49,
        0x72, 0x83, 0xcf, 0x4b, 0xae, 0x53, 0xea, 0xd2,
        0x6d, 0x4a, 0x79, 0x9a, 0x49, 0x61, 0xc4, 0x43,
        0x8a, 0x4a, 0x04, 0xad, 0xe2, 0x78, 0x08, 0x8b,
        0xda, 0xff, 0x0e, 0x05, 0xea, 0xf9, 0xfb, 0x4d,
        0x1f, 0xda, 0x9d, 0x6b, 0xcb, 0x3b, 0xd1, 0xf0,
        0x93, 0xa8, 0xbb, 0x63, 0xb5, 0x6f, 0x0c, 0xe5,
        0x6c, 0x36, 0xee, 0x13, 0x84, 0xa8, 0xd9, 0xc8,
        0x27, 0x2f, 0x70, 0x7f, 0xcb, 0x72, 0xa3, 0xb5,
        0x4c, 0x09, 0x8c, 0x35, 0x94, 0x9b, 0x08, 0xb1,
        0x9f, 0x84, 0x65, 0x8a
    };
    PI_AesCtrCrypt(key, iv, long_input, sizeof(long_input));
    EXPECT_EQ(memcmp(long_input, long_want, sizeof(long_input)), 0);
    PI_AesCtrCrypt(key, iv, long_input, sizeof(long_input));
    EXPECT_EQ(memcmp(long_input, long_plaintext, sizeof(long_input)), 0); // test decryption
}
